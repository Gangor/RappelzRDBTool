local encodeMap = {}
local decodeMap = {}

local function compute_bit_swapping()
	local j, oldValue

	for i = 0,31 do
		encodeMap[i] = i;
	end

	j = 3
	for i = 0,31 do
		oldValue = encodeMap[i]
		encodeMap[i] = encodeMap[j]
		encodeMap[j] = oldValue
		j = (j + i + 3) % 32
	end

	for i = 0,31 do
		decodeMap[encodeMap[i]] = i
	end
end

compute_bit_swapping()

local script_on_dead_mapping

rdb.convertData = function (dst, mode, row, rowNum)
	if mode == DCT_Read and dst == DF_RDB then
		local value = row.id
		local result = 0
		for i = 0,31 do
			if (1 << i) & value ~= 0 then
				result = result | (1 << decodeMap[i])
			end
		end
		row.id = result

		if script_on_dead_mapping[result] then
			row.script_on_dead = script_on_dead_mapping[result]
		end
	elseif mode == DCT_Write and dst == DF_RDB then
		local value = row.id
		local result = 0
		for i = 0,31 do
			if (1 << i) & value ~= 0 then
				result = result | (1 << encodeMap[i])
			end
		end
		row.id = result
		row.padding0 = 0
		row.padding1 = 0
	end
end

